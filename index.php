<script>
    var modelosPorMarca = <?php echo json_encode($modelos); ?>;


    function desbloquearidmarca(idmarca) {
        let selectModelo = document.getElementById("modelo");

        // Limpiar opciones anteriores
        selectModelo.innerHTML = '<option value="">Seleccione un modelo:</option>';

        // Filtrar modelos según la marca seleccionada
        let modelosFiltrados = modelosPorMarca.filter(modelo => modelo.idmarca == idmarca);

        // Agregar opciones al selector de modelos
        modelosFiltrados.forEach(modelo => {
            let option = document.createElement("option");
            option.value = modelo.idmodelo;
            option.textContent = modelo.nombre;
            selectModelo.appendChild(option);
        });

        // Habilitar el selector de modelos si hay opciones disponibles
        selectModelo.disabled = modelosFiltrados.length === 0;
    }
</script>